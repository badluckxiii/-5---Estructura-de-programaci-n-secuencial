#  Calcular el sueldo mensual de un operario conociendo la cantidad de horas trabajadas y el valor por hora. 
cantidadh=int(input('Introduce la cantidad de horas trabajadas: '))
sueldoh=int(input('Introduce el sueldo hora: '))
sueldom=cantidadh*sueldoh
print('El salario mensual es igual a ',sueldom)